int main(void)
{
	/****************** Operation System Initialization ***************/    
	OS_ERR err;
	CPU_SR_ALLOC();	
	OSInit(&err);		//初始化UCOSIII    
	/******************	Operation  Task **************/
	OS_CRITICAL_ENTER();		//进入临界区    
	/****************** Persional Init	Code **************/
	BSP_Init();  					//初始化各外设       
	create_star_task(40);         
	OS_CRITICAL_EXIT();	//退出临界区	 
	/****************** Open UCOSIII System ****************/
	OSStart(&err);  
	while(1);
}

